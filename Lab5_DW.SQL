CREATE TABLE CITYDT(
    CITYID NUMBER(30,0) PRIMARY KEY,
    ZIPCODE VARCHAR2(60),
    STATE VARCHAR2(60)
);
CREATE TABLE ANIMALDT(
    ANIMALID NUMBER(30,0) PRIMARY KEY,
    CATEGORY VARCHAR2(60),
    BREED VARCHAR2(60),
    REGISTERED VARCHAR2(60),
    LISTPRICE NUMBER(38,4)
);
CREATE TABLE MERCHANDISEDT(
    ITEMID NUMBER(30,0) PRIMARY KEY,
    QUANTITYONHAND NUMBER(30,0),
    CATEGORY VARCHAR2(60)
);
CREATE TABLE SALEANIMALDT(
    SALEANIMALID NUMBER(30,0) PRIMARY KEY,
    SALEPRICE NUMBER(38,4),
    CONSTRAINT SALEANIMALDT_FK_ANIMALDT FOREIGN KEY (SALEANIMALID)
        REFERENCES ANIMALDT(ANIMALID)
);
CREATE TABLE EMPLOYEEDT(
    EMPLOYEEID NUMBER(30,0) PRIMARY KEY,
    CITYID NUMBER(30,0),
    CONSTRAINT EMPLOYEEDT_FK_CITYID FOREIGN KEY(CITYID)
        REFERENCES CITYDT(CITYID)
);
CREATE TABLE CUSTOMERDT(
    CUSTOMERID NUMBER(30,0) PRIMARY KEY,
    CITYID NUMBER(30,0),
    CONSTRAINT CUSTOMERDT_FK_CITYID FOREIGN KEY(CITYID)
        REFERENCES CITYDT(CITYID)
);
CREATE TABLE SUPPLIERDT(
    SUPPLIERID NUMBER(30,0) PRIMARY KEY,
    CITYID NUMBER(30,0),
    CONSTRAINT SUPPLIERDT_FK_CITYID FOREIGN KEY(CITYID)
        REFERENCES CITYDT(CITYID)
);
CREATE TABLE MERCHANDISEORDERDT(
    PONUMBER NUMBER(30,0) PRIMARY KEY,
    SUPPID NUMBER(30,0),
    EMPID NUMBER(30,0),
    CONSTRAINT MERCHANDISEORDERDT_FK_SUPPID FOREIGN KEY(SUPPID)
        REFERENCES SUPPLIERDT(SUPPLIERID),
    CONSTRAINT MERCHANDISEORDERDT_FK_EMPID FOREIGN KEY(EMPID)
        REFERENCES EMPLOYEEDT(EMPLOYEEID)
);
CREATE TABLE ORDERSFT(
    ORDERID NUMBER(30,0) PRIMARY KEY,
    SUPPLIERID NUMBER(30,0),
    EMPLOYEEID NUMBER(30,0),
    PONUMBER NUMBER(30,0),
    CONSTRAINT ORDERFT_FK_SUPPLIERID FOREIGN KEY (SUPPLIERID)
        REFERENCES SUPPLIERDT(SUPPLIERID),
    CONSTRAINT ORDERFT_FK_EMPLOYEEID FOREIGN KEY (EMPLOYEEID)
        REFERENCES EMPLOYEEDT(EMPLOYEEID),
    CONSTRAINT ORDERFT_FK_PONUMBER FOREIGN KEY (PONUMBER)
        REFERENCES MERCHANDISEORDERDT(PONUMBER)
);
CREATE TABLE SALESFT(
    SALEID NUMBER(30,0) PRIMARY KEY,
    CUSTOMERID NUMBER(30,0),
    ITEMID NUMBER(30,0),
    ANIMALID NUMBER(30,0),
    CONSTRAINT SALEST_FK_CUSTOMERID FOREIGN KEY (CUSTOMERID)
        REFERENCES CUSTOMERDT(CUSTOMERID),
    CONSTRAINT SALESFT_FK_ITEMID FOREIGN KEY (ITEMID)
        REFERENCES MERCHANDISEDT(ITEMID),
    CONSTRAINT SALESFT_FK_ANIMALID FOREIGN KEY (ANIMALID)
        REFERENCES ANIMALDT(ANIMALID)
);
